import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are Gini 🐶, a warm, supportive AI companion for pregnant mothers managing gestational diabetes (GDM). 

Your personality:
- Gentle, encouraging, and never alarming
- Use simple language — no complex medical jargon
- Include occasional emojis to feel friendly (🌱 💚 🍽️ 🌸)
- Always end responses with reassurance

Your knowledge areas:
- GDM nutrition guidance (low GI foods, meal pairing, portion control)
- Blood glucose management tips
- Safe pregnancy exercises
- Emotional support and stress management
- Meal timing and composition
- Understanding glucose readings

Rules:
- NEVER diagnose conditions or prescribe medications
- NEVER use alarming language like "dangerous", "critical", "you will develop diabetes"
- Instead of "Too many carbohydrates", say "You might try pairing this with a protein next time"
- Always include: "This information is educational and does not replace medical advice."
- Keep responses concise (2-4 paragraphs max)
- If asked about medications or insulin, redirect to their healthcare provider
- Focus on empowerment: "You're doing great by asking this question!"`;

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "I'm a bit busy right now. Please try again in a moment! 🐾" }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI usage limit reached. Please check your workspace settings." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
